                                                Magyar válogatott labdarúgó, a TSG 1899 Hoffenheim csatára</p>
                                            <p style="text-align: left;">&#8220;Mindig arra törekedtem, hogy a legtöbbet hozzam ki minden pillanatból, legyen szó munkáról, a szeretteimmel együtt töltött időről, vagy a kevésbé sikeres időszakokról. Eddigi pályafutásom alatt mindig a nehéz helyzetekből merítettem a legtöbb erőt a folytatáshoz.</p>
                                            <p style="text-align: left;">Ezért is igyekszem segíteni azoknak, akik támogatásra szorulnak: egy barátnak, egy utcán élő embertársunknak, vagy éppen kisgyerekeknek, akik rajonganak a fociért. Előzékenyen magunk elé engedni valakit a sorban, pár szót váltani Veled, a szurkolóval &#8211; a mindennapi segítségnyújtásnak, örömszerzésnek számos formája van. A legőszintébb örömet mindig akkor éreztem igazán, ha feltétel nélkül segíthettem másoknak.</p>
                                            <p style="text-align: left;">Hálás vagyok az életnek, a sorsnak és a kitartásomnak a sikereimért. Utunk azonban nem teljes attól, hogy elvégezzük a saját feladatunkat, nagyon sokan vannak, akik támogatásra szorulnak. Hiszem azt, hogy nagyobb összefogással megkönnyíthetjük az életét azoknak, akik igazán nehéz helyzetben vannak és mosolyt csalhatunk az arcukra.</p>
                                            <p style="text-align: left;">Hálás vagyok önkéntes csapatomnak, akik csatlakoztak hozzám, hogy minél többet tudjunk adni azoknak, akik rászorulnak. Gyere Te is és mutassuk meg együtt, hogy odafigyeléssel, összefogással egy élhetőbb, boldogabb világot teremthetünk!&#8221;</p>
